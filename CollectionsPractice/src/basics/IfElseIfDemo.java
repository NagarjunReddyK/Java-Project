package basics;

public class IfElseIfDemo {
	public static void main(String[] args) {
		int count=30;
		if(count==10){
			System.out.println("Count is 10");
		}
		else if(count==20){
			System.out.println("Count is 20");
		}
		else if(count==30){
			System.out.println("Count is 30");
		}
		else
			System.out.println("This is else statement...");

	}

}
